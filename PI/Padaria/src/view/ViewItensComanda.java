    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTItensComanda.getModel();
        modelo.setNumRows(0);
        ItensComandaDAO idao = new ItensComandaDAO();
        for (ItensComanda ic : idao.read()) {
            modelo.addRow(new Object[]{
                ic.getId(),
                ic.getIdComanda(),
                ic.getIdProduto(),
                ic.getQuantidade()
            });
        }
    }
    
