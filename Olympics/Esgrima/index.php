					ganha pontos, quando toca a região do tronco do adversário com o florete. A espada pode ser usada para atingir
					qualquer parte do corpo do adversário. Já o sabre (com até 1,05 metro) pode ser usado para atingir, com a ponta ou
					lâmina, a região da cintura ou acima.</p>
				<p>Cada vez que um esgrimista <strong>toca o colete do adversário</strong> é marcado o ponto eletronicamente,
					através de sensores.Ganha o combate, o esgrimista que somar mais pontos. Os esgrimistas usam roupas especiais para
					o combate, com o propósito de evitar ferimentos. Máscara metálica, luvas e colete protetor são equipamentos
					obrigatórios para os homens. Além destes equipamentos, as mulheres usam também
					protetores para os seios.</p>
